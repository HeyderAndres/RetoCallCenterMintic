    private void jButtonProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProcesarActionPerformed
        DefaultListModel  model = new DefaultListModel();
        ArrayList<Empleado> listEmpleado = new ArrayList<>();
        
        int index = 0;
        for (Empleado empleado : empleadoLista) {
            double valorLiquidarEmpleado = liquidacion.liquidarNominaEmp(empleado);
            double valorPrestacionEmpleado = liquidacion.liquidarPrestacionesempd(empleado);
            String data = (index+1)+") - Id: "+empleado.getId() +" - Nombre: "+ 
                            empleado.getNombre()+"- Apellido: "+ empleado.getApellido()+
                            " - Cedula: \n"+ empleado.getCedula()+ " - liquidacion: " + Math.round(valorLiquidarEmpleado)+
                            " - Prestaciones: "+ Math.round(valorPrestacionEmpleado);
            model.add(index, data);
            listEmpleado.add(empleado);
            index++;
            
        }
        jListLiquidacion.setModel(model);
        // TODO add your handling code here:
       
    }//GEN-LAST:event_jButtonProcesarActionPerformed

