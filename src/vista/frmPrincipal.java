    private void jButtonConsultarEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarEditarActionPerformed
        // TODO add your handling code here:
         if (jTextFieldCodigoEditar.getText().equals("")) {
           empleado = modelEmpleado.validarCedula(jTextFieldCedulaEditar.getText());
        }else
            if (jTextFieldCedulaEditar.getText().equals("")) {
            empleado = modelEmpleado.consultaEmpleadoId(jTextFieldCodigoEditar.getText());
        }
        if (empleado == null) {
            JOptionPane.showMessageDialog(rootPane, "empleado no registrado");
            jTextFieldNombreEditar.setText("");
            jTextFieldApellidoEditar.setText("");
            jTextFieldSalarioEditar.setText("");
            jTextFieldHoraExtraEditar.setText("");
            jCheckBoxAuxilioTransporteEditar.setSelected(false);
        }else{
            jTextFieldCodigoEditar.setText(empleado.getId());
            jTextFieldNombreEditar.setText(empleado.getNombre());
            jTextFieldApellidoEditar.setText(empleado.getApellido());
            jTextFieldSalarioEditar.setText(String.valueOf(empleado.getSalario()));
            jTextFieldHoraExtraEditar.setText(String.valueOf(empleado.getHorasExtra()));
            jTextFieldCedulaEditar.setText(empleado.getCedula());
            jCheckBoxAuxilioTransporteEditar.setSelected(empleado.isAuxilioTransporte());
            jTextFieldCodigoEditar.setEditable(false);
        }
    }//GEN-LAST:event_jButtonConsultarEditarActionPerformed

    private void jButtonGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarCambiosActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextFieldNombreEditar.getText().equals("") || 
                jTextFieldApellidoEditar.getText().equals("") ||
                 jTextFieldCedulaEditar.getText().equals("") ||
                Double.valueOf(jTextFieldSalarioEditar.getText())<= 0 ||
                Float.valueOf(jTextFieldHoraExtraEditar.getText())< 0
                ) {
                JOptionPane.showMessageDialog(rootPane,"Debe completar todos los campos y el salario debe ser mayor que cero");
            }else{
                if(modelEmpleado.editarEmpleado(Integer.valueOf(jTextFieldCodigoEditar.getText()),
                    jTextFieldNombreEditar.getText(), jTextFieldApellidoEditar.getText(),
                    Float.valueOf(jTextFieldHoraExtraEditar.getText()),
                    jCheckBoxAuxilioTransporteEditar.isSelected(),
                    Double.valueOf(jTextFieldSalarioEditar.getText()),
                    jTextFieldCedulaEditar.getText())){

                JOptionPane.showMessageDialog(rootPane,"Usuario modificado");
                jTextFieldNombreEditar.setText("");
                jTextFieldCodigoEditar.setText("");
                jTextFieldApellidoEditar.setText("");
                jTextFieldSalarioEditar.setText("0");
                jTextFieldHoraExtraEditar.setText("0");
                jTextFieldCedulaEditar.setText("");
                jCheckBoxAuxilioTransporteEditar.setSelected(false);
                jTextFieldCodigoEditar.setEditable(true);
                this.empleadoLista = modelEmpleado.listaEmpleados();
                this.listaEmpleado();
            }else{
                JOptionPane.showMessageDialog(rootPane,"verifique los datos Usuario NO actualizado");
            }

        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,"Error: Usuario no Se Actualizo");
        }
    }//GEN-LAST:event_jButtonGuardarCambiosActionPerformed

